package com.example.visma.testapp.ChatFeature;

public class Group {



    String groupName;
    String groupID;

    public Group(String groupName, String groupID) {
        this.groupName = groupName;
        this.groupID = groupID;
    }

    public String getGroupName() {
        return groupName;
    }

    public String getGroupID() {
        return groupID;
    }

}
